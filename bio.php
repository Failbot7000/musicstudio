    <h2>Biography</h2>
    <p>Daniel Kuzuhara is a pianist born in Madison, Wisconsin who graduated with a master’s degree in Piano Performance from the Indiana University Jacobs School of Music, studying under Dr. Karen Shaw. Prior to his studies in Indiana, he studied under Professor Anthony Padilla at Lawrence University, Julie Chang, and Karen Hill. He currently studies under Mastero Sebastiaan Bijlsma of Tiziano Rosetti’s International Music Academy.</p>

    <p>Throughout his career, Daniel has received multiple awards and honors at local, national, and international events. Most notably, Daniel won 1st prize at the Golden Classical Music Awards International Competition, Grand Prize and 1st Place Award at the Rocky Mountain Summer Music Competition, 1st prize in the International Music Competition “Vienna” Grand Prize Virtuoso, two Platinum medal Awards at the 2021 Quebec International Music Competition, Platinum Medal Award in the Global International Music Competition, Gold Medal Awards at the 2021 and 2022 Grand Metropolitan International Music Competition, 1st prize at the 2022 Tiziano Rosetti’s International Music Competition, 2nd Prize in the Gloria Artis Competition in Vienna, 2nd Prize at the Franz Liszt Center International Music Competition; and 2nd place in the 2021 Music International Grand Prix.</p>

    <p>Locally, Daniel won the 2011 Neale-Silva competition sponsored by Wisconsin Public Radio and was featured in a live performance on WPR. </p>

    <p>Daniel has performed recitals in multiple venues locally, nationally and internationally. His most notable performance venues are Carnegie Hall in New York and Musikverein in Vienna, Austria. </p>

    <p>Daniel runs his own music studio offering private lessons for piano and violin called Appassionato Music Studio. He currently teaches students of various experience levels ages 4 through adults. Daniel has co-founded the Apostle Island trio, who gave their debut performance in 2017. He gives solo and chamber performances throughout the year and has taken leadership roles in multiple musical events in the Madison area. As a leader, he is a co-chair of the Madison Area Music Educators (MAME) Honors Festival and the former treasurer of the organization.</p>

    <p>Outside of music, Daniel enjoys role playing and board games, spending time with family, and training in Taekwondo.</p>
    
    <p>Daniel looks forward to educating, inspiring, and sharing music with the community for many years to come.</p>
