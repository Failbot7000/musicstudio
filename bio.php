    <h2>Biography</h2>

    <p>Daniel Kuzuhara is an American pianist born in Madison, Wisconsin who graduated with a master’s degree in Piano Performance from the Indiana University Jacobs School of Music, studying under Dr. Karen Shaw. Prior to his studies in Indiana, he studied under Professor Anthony Padilla at Lawrence University, Julie Chang, and Karen Hill. He currently studies under Maestro Sebastiaan Bijlsma of Tiziano Rosetti’s International Music Academy in Lugano, Switzerland. He also takes many masterclasses with the Director Tiziano Rosetti himself. </p>

    <p>Throughout his career, Daniel has won over 25 awards in international competitions. Most notably, Daniel won 1st prize at the Golden Classical Music Awards International Competition, Grand Prize Award at the Rocky Mountain Summer Music Competition, 1st prize in the International Music Competition “Vienna” Grand Prize Virtuoso, 1st prize at the 2022 Tiziano Rosetti’s International Music Competition, two Platinum medal Awards at the 2021 Quebec International Music Competition, Gold Medal Awards at the 2021 and 2022 Grand Metropolitan International Music Competition, 2nd Prize in the Gloria Artis Competition in Vienna, and 2nd place in the 2021 Music International Grand Prix. Locally, Daniel won the 2011 Neale-Silva competition sponsored by Wisconsin Public Radio and was featured in a live performance on WPR. </p>

    <p>Daniel has performed recitals in multiple venues locally, nationally and internationally. His most notable performance venues are Carnegie Hall in New York and Musikverein in Vienna, Austria. </p>

    <p>Outside of performances, he runs his own music studio in Madison called Appassionato Music Studio, teaching private piano and violin lessons. He teaches students of all experience levels ages 4 through adults and is currently accepting new students. </p>
    
    <p>He looks forward to educating, inspiring, and sharing music with the community for many years to come.</p>
