		    <h2>About Appassionato Music Studio</h2>
    
    <img src="images/photos/studio.jpg" width="360px;" style="float: right; margin: 10px;" />
    
    <p>The studio founded by Daniel Kuzuhara began in July of 2016, where he began teaching private piano and violin lessons. The name Appassionato Music Studio was given in February of 2017 to better symbolize the core values of the studio. “Appassionato” is the Italian word for passionate or with passion. This word demonstrates the studio’s desire to create passion and emotion within the students’ music. The studio strives for the right balance between enjoyment of the art and maintaining a high level of playing from the students. Appassionato Music Studio constantly invites challenges and creativity while setting high yet reasonable goals for each student to maximize the amount of progress, reward, and satisfaction that comes with the journey of learning music. </p>

    <p>Appassionato Music Studio currently contains over 25 students ranging from age 4 through adults. Students of Appassionato Music Studio have shown great achievement throughout the studio’s 5+ years. In addition to the strong performances they demonstrate in their studio recitals, they also attend multiple other events and have received recognition for their hard work. Students have received top marks in the National Federation of Music Clubs (NFMC) Junior Festival. Students of the studio also hold a 100% pass rate in the Associated Board of the Royal School of Music (ABRSM) practical exams. In these exams, multiple students passed with merit (honors) and four passed with distinction (top honors). One student qualified for Wisconsin School of Music Association’s (WSMA) State Solo and Ensemble in the district event in 2019. In competitive events, five students have been recognized with trophy winning performances at the Rhapsody Arts Center’s Sonatina Festival, with one taking first prize in the age 5-6 category. One student also won first place in the WSMA Composition Festival in the Spring of 2021, 4th-6th Grade division. Finally, a student won first prize at the 2018 Steinway Piano Competition in Madison, competing in the Division I category.</p> 

    <p>Appassionato Music Studio currently offers private piano lessons, violin lessons, ABRSM theory tutoring, accompanying services, and his own services as a performer for any concerts/events. The studio’s services are open to children and adults of all ages. Students of all experience levels are welcome, including beginners.</p>

        
